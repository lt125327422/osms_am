package com.itecheasy.core.amazon.vo;

import java.util.Date;

/**
 * @Auther: liteng
 * @Date: 2018/7/2 08:48
 * @Description:
 */
public class RequestReportResultVO {

    private String reportRequestId;

    private String reportType;

    private Date startDate; 	//用于选择待报告数据日期范围的起始日期。

    private Date endDate;// 	用于选择待报告数据日期范围的结束日期。

    private boolean scheduled; //	布尔值，用来指明是否已计划报告。如果已计划报告，则值为 true；否则值为 false。

    private boolean submittedDate;//	提交报告的日期。

    private String ReportProcessingStatus; //报告的处理状态。

    public String getReportRequestId() {
        return reportRequestId;
    }

    public void setReportRequestId(String reportRequestId) {
        this.reportRequestId = reportRequestId;
    }

    public String getReportType() {
        return reportType;
    }

    public void setReportType(String reportType) {
        this.reportType = reportType;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public boolean isScheduled() {
        return scheduled;
    }

    public void setScheduled(boolean scheduled) {
        this.scheduled = scheduled;
    }

    public boolean isSubmittedDate() {
        return submittedDate;
    }

    public void setSubmittedDate(boolean submittedDate) {
        this.submittedDate = submittedDate;
    }

    public String getReportProcessingStatus() {
        return ReportProcessingStatus;
    }

    public void setReportProcessingStatus(String reportProcessingStatus) {
        ReportProcessingStatus = reportProcessingStatus;
    }
}
